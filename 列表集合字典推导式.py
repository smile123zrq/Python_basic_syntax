list1 = [1, 32, 53, 8, 46, 97]
# 列表推导式,可以很方便的根据一些条件来筛选列表
list2 = [i for i in list1 if i < 50]
print(list2)
# [1, 32, 8, 46]

# 集合推导式
a = {3, 89, 23, 645, 90, 55}
b = {i for i in a if i > 60}
print(b)
# {89, 90, 645}

# 字典推导式
c = {i: i**2 for i in a if i < 60}
print(c)
# {3: 9, 23: 529, 55: 3025}

# 元组不可改变，元组没有推导式
